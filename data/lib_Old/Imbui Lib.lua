function imbu_type(cid, item, confirmImbue)
	if not confirmImbue then
		confirmImbue = 0
	end


	local str = ''
	local store = 0
	local id = 0
	local id2 = 0


	isImbueableItem(item)
	if getPlayerStorageValue(cid, 854827) ~= '' then
		if not doCutText(getPlayerStorageValue(cid, 854827),"*") then
			print("Algo deu errado com o jogador " .. getPlayerName(cid) .. " | Verifique a STORAGE 854827.")
			return false
		end
	    local ItemBonusList = doCutText(getPlayerStorageValue(cid, 854827),"*")
	    
        texto = ItemBonusList[2]
        if texto ~= '' then
           	if tonumber(doCutText(texto,",")[1]) then
           		id = tonumber(doCutText(texto,",")[1])
			end
		end

		texto2 = ItemBonusList[3]
        if texto2 ~= '' then
           	if tonumber(doCutText(texto2,",")[1]) then
           		id2 = tonumber(doCutText(texto2,",")[1])
			end
		end
		----- Verificar se o item já possui algum imbuiment semelhante
		if id ~= 0 then
			--doBroadcastMessage("")
			for l, s in pairs(imbu_Bonus) do
				for j = 1, #s.items do
					if itemType == s.items[j] then						
						--doBroadcastMessage(" l = " .. getBonusId(l) .. " | id = " .. id)
						if ((getBonusId(l) >= 17 and getBonusId(l) <= 21) and ((id >= 17 and id <= 21) or (id2 >= 17 and id2 <= 21))) or (getBonusId(l) == id or getBonusId(l) == id2) then
							str = str
							store = store
						else
							if str == '' then
								str = '* '..l..' * '
							else
								str = str ..l..' * '
							end
				
							------ Salvando o bonus na storage ----								
							if store ~= 0 then
								store = store.."/"..getBonusId(l)
							else
								store = getBonusId(l)
							end						
							
						end
					end
				end
			end
		end
	else
		for i, v in pairs(imbu_Bonus) do
			for j = 1, #v.items do
				if itemType == v.items[j] then
					if str == '' then
						str = ' * '..i..' * '
					else
						str = str ..i..' * '
					end
					------ Salvando o bonus na storage ----				
					for k, l in pairs(bonus_storage_value) do
						if k == i then
							if store ~= 0 then
								store = store.."/"..bonus_storage_value[k]
							else
								store = bonus_storage_value[k]
							end						
						end
					end
				end
			end
		end
	end

	--doBroadcastMessage(str)
	--doBroadcastMessage(store)
	setPlayerStorageValue(cid, Bonus_storage, store)
	BonusList = str
	return BonusList
end


